package servicios;

/**
 * Interfaz cuya función es dar servicio al menú, tiene solo la cabecera (sin
 * entrar en detalle) de los métodos
 */
public interface MenuInterface {

	/**
	 * Método encargado de mostrar al usuario un menú por consola con las opciónes
	 * disponibles
	 * 
	 * @return Número entero en relación a la opción seleccionada
	 */
	int MostrarMenu();

}
